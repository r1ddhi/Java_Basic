package org.Calculator;
public class Arithmatic {
    public static int add(int a,int b){
        return a+b;
    }
    public static int subtract(int a,int b)
    {
        return a-b;
    }
    public static int multiply(int a, int b )
    {
        return a*b;
    }
    public static float division(float a,float b){
        return a/b;
    }
}
